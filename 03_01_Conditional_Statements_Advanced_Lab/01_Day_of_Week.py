day_input = int(input())

if day_input == 1:
    result = 'Monday'
elif day_input == 2:
    result = 'Tuesday'
elif day_input == 3:
    result = 'Wednesday'
elif day_input == 4:
    result = 'Thursday'
elif day_input == 5:
    result = 'Friday'
elif day_input == 6:
    result = 'Saturday'
elif day_input == 7:
    result = 'Sunday'
else:
    result = 'Error'

print(result)
